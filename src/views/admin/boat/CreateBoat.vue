    ...mapActions('boats', ['storeBoat']),

    async uploadImage(e) {
      this.boat.file = e.target.files[0];
    },
    // Create Boat in BDD
    async storeBoat(e){
      e.preventDefault();
      const config = {
          headers: {
              'content-type': 'multipart/form-data'
          }
      }
      let boatItem = { name: this.boat.name, picture: this.boat.file, description_fr: this.boat.description_fr, description_en: this.boat.description_en,type_fr: this.boat.type_fr, type_en: this.boat.type_en, displacements: this.boat.displacements, surface: this.boat.surface, engine_power: this.boat.engine_power, hull_fr: this.boat.hull_fr, hull_en: this.boat.hull_en, deck_fr: this.boat.deck_fr, deck_en: this.boat.deck_en, mast_fr: this.boat.mast_fr, mast_en: this.boat.mast_en, architect: this.boat.architect, diverse_fr: this.boat.diverse_fr, diverse_en: this.boat.diverse_en}
      // console.log(boatItem)
      await axiosClient.post('boat', boatItem, config)
      .then(res => {
        console.log(res)
        router.push(`/boat`)
      })
      .catch(err => alert(err.response.data.message));
    },

  },
